// === GLOBAL SERVICE TRIGGER ACTION ===
window.triggerServiceAction = function(serviceType, modelKey) {
    // Close modal if open
    const promoModal = document.getElementById('promo-modal');
    if (promoModal) promoModal.classList.remove('open');

    // If modelKey is specified, switch showcased model
    if (modelKey) {
        window.switchModel(modelKey);
    }

    // Set service value in radio buttons
    const serviceRadio = document.querySelector(`input[name="form_service"][value="${serviceType}"]`);
    if (serviceRadio) {
        serviceRadio.checked = true;
        const parentLabel = serviceRadio.closest('.radio-option');
        if (parentLabel) {
            parentLabel.parentElement.querySelectorAll('.radio-option').forEach(el => el.classList.remove('selected'));
            parentLabel.classList.add('selected');
        }
    }

    // Scroll to form smoothly
    const formBooking = document.getElementById('form-booking');
    if (formBooking) {
        setTimeout(() => {
            formBooking.scrollIntoView({ behavior: 'smooth', block: 'start' });
            // Focus first input field
            const nameInput = document.getElementById('form-name');
            if (nameInput) nameInput.focus();
        }, modelKey ? 300 : 0);
    }
};

